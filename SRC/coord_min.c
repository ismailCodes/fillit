#include "fillit.h"

int		coord_min(int *tab)
{
	int		i;
	int		min;

	i = 0;
	min = tab[i];
	while (i < 4)
	{
		if (tab[i] < min)
			min = tab[i];
		i++;
	}
	return (min);
}
